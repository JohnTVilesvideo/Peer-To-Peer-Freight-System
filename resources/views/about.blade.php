<h3>Welcom to Peer To Peer Freight System</h3>
    <p>There are three kinds of roles in this system: Poster, Driver, Admin</p>
    <p>As a poster, you can see all the routes provided by the drivers, and you can place an order for them. You
    can also cancel your order</p>
    <p>As a driver, you can edit about your routes, accept or reject an order from the poster, process an order</p>
    <p>As an admin, you can edit the routes, users, etc.</p>
    <p>You can only register as a poster or driver, you can't register as a admin, you need to contact the website master
    to add you as an administrator.</p>
